    private void signinbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signinbtnActionPerformed
        // TODO add your handling code here:
        String email = emailtxt.getText();
        String pass = passtxt.getText();
        
        if(client.isSelected())
        {
            c = 1; v = 0;
         customerinfo cusin= new customerinfo(getcon.getConnection());
         cus = cusin.getcustomer(email, pass);
         if(cus==null)
         {
            
             Thread th=new Thread(){
                @Override
                public void run(){
                    try{
                           
                        loading ff = new loading();
                        ff.setVisible(true);
                            Thread.sleep(500);
                        ff.setVisible(false);
                        new invaliddialog().setVisible(true);
                        }catch(Exception e){
                        e.printStackTrace();
                    }}
                
            };th.start();
             
         }
         else {
         
         this.setVisible(false);
         Thread th=new Thread(){
                @Override
                public void run(){
                    try{
                           
                        loading ff = new loading();
                        ff.setVisible(true);
                        Thread.sleep(500);
                        ff.setVisible(false);
                        home fr = new home();
                        fr.setVisible(true);
                        }catch(Exception e){
                        e.printStackTrace();
                    }}
                
            };th.start();
         }
        }
        else if(vendor.isSelected())
        {
         v=1; c=0;
         vendorinfo venin= new vendorinfo(getcon.getConnection());
         ven =venin.getvendor(email, pass);
         if(ven==null)
         {
            
             Thread th=new Thread(){
                @Override
                public void run(){
                    try{
                           
                            loading ff = new loading();
                        ff.setVisible(true);
                        Thread.sleep(500);
                        ff.setVisible(false);
                        new invaliddialog().setVisible(true);
                        }catch(Exception e){
                        e.printStackTrace();
                    }}
                
            };th.start();
             
         }
         else {
         this.setVisible(false);
         Thread th=new Thread(){
                @Override
                public void run(){
                    try{
                           
                        loading ff = new loading();
                        ff.setVisible(true);
                        Thread.sleep(500);
                        ff.setVisible(false);
                        vendorhome fr = new vendorhome();
                        fr.setVisible(true);
                        }catch(Exception e){
                        e.printStackTrace();
                    }}
                
            };th.start();
         }
        }
        else 
        {
             c = 0;
             v = 0;
        }
    }//GEN-LAST:event_signinbtnActionPerformed

